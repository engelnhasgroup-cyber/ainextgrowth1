// Trinity Bundle download utilities (client-side)
// Generates the 3 Markdown files and triggers downloads, then records the event.

import type { ItemDetail } from '@/lib/types'

const BRAND = 'NexusAI 2026'
const SITE_URL = 'https://nexusai2026.example.com'

function fileHeader(item: ItemDetail, fileLabel: string): string {
  const date = new Date().toISOString().slice(0, 10)
  return [
    `---`,
    `title: "${item.title.replace(/"/g, "'")}"`,
    `type: "${item.type}"`,
    `file: "${fileLabel}"`,
    `category: "${item.category}"`,
    `niche: "${item.niche}"`,
    `audience: "${item.audience}"`,
    `difficulty: "${item.difficulty}"`,
    `tags: [${item.tags.map((t) => `"${t}"`).join(', ')}]`,
    `required_tools: [${item.requiredTools.map((t) => `"${t}"`).join(', ')}]`,
    `generated_by: "${BRAND}"`,
    `source: "${SITE_URL}"`,
    `generated_date: "${date}"`,
    `---`,
    ``,
  ].join('\n')
}

function backlinkFooter(item: ItemDetail): string {
  return [
    ``,
    `---`,
    ``,
    `> **Generated by [${BRAND}](${SITE_URL})** — The Largest Autonomous AI Prompt & Skill Library for 2026.`,
    `> ${item.type === 'prompt' ? 'Prompt' : 'Skill'} slug: \`${item.slug}\` · Category: \`${item.category}\``,
    `> Discover 200 fresh trending prompts & skills generated daily by our AI Agent.`,
    `> Download more Trinity Bundles (Prompt + Workflow + Audience) at ${SITE_URL}.`,
    ``,
  ].join('\n')
}

export function buildPromptFile(item: ItemDetail): string {
  return [
    fileHeader(item, '1_The_Prompt'),
    `# ${item.title}`,
    ``,
    `> ${item.summary}`,
    ``,
    item.promptContent,
    backlinkFooter(item),
  ].join('\n')
}

export function buildWorkflowFile(item: ItemDetail): string {
  return [
    fileHeader(item, '2_Workflow_and_Execution'),
    `# Workflow & Execution — ${item.title}`,
    ``,
    item.workflowContent,
    backlinkFooter(item),
  ].join('\n')
}

export function buildAudienceFile(item: ItemDetail): string {
  return [
    fileHeader(item, '3_Target_Audience_and_Use_Cases'),
    `# Target Audience & Use Cases — ${item.title}`,
    ``,
    item.audienceContent,
    backlinkFooter(item),
  ].join('\n')
}

function triggerDownload(filename: string, content: string) {
  const blob = new Blob([content], { type: 'text/markdown;charset=utf-8' })
  const url = URL.createObjectURL(blob)
  const a = document.createElement('a')
  a.href = url
  a.download = filename
  document.body.appendChild(a)
  a.click()
  document.body.removeChild(a)
  setTimeout(() => URL.revokeObjectURL(url), 1500)
}

export function safeFilename(s: string): string {
  return s
    .toLowerCase()
    .replace(/[^a-z0-9\s-]/g, '')
    .trim()
    .replace(/\s+/g, '-')
    .replace(/-+/g, '-')
    .slice(0, 60)
}

export type DownloadChoice = 'all' | 'prompt' | 'workflow' | 'audience'

export async function downloadBundle(item: ItemDetail, choice: DownloadChoice) {
  const base = safeFilename(item.slug || item.title)
  const files: { name: string; content: string }[] = []

  if (choice === 'all' || choice === 'prompt') {
    files.push({ name: `1_The_Prompt_${base}.md`, content: buildPromptFile(item) })
  }
  if (choice === 'all' || choice === 'workflow') {
    files.push({ name: `2_Workflow_and_Execution_${base}.md`, content: buildWorkflowFile(item) })
  }
  if (choice === 'all' || choice === 'audience') {
    files.push({ name: `3_Target_Audience_and_Use_Cases_${base}.md`, content: buildAudienceFile(item) })
  }

  // Stagger downloads slightly so browsers don't block multi-download
  for (let i = 0; i < files.length; i++) {
    const f = files[i]
    setTimeout(() => triggerDownload(f.name, f.content), i * 450)
  }

  // Record download event server-side
  try {
    fetch('/api/download', {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify({ id: item.id }),
    })
  } catch {}
}
